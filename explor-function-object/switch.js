var color = 'black';

if(color == 'blue'){
    console.log('color is blue');
}
else if(color == 'red'){
    console.log('color is red');
}
else if(color == 'black'){
    console.log('color is black');
}
else{
    console.log('no color');
}


switch (color){
    case 'blue':
        console.log('color is blue');
        break;
    case 'red':
        console.log('color is red');
        break;
    case 'black':
        console.log('color is black');
        break;
    case 'yellow':
        console.log('color is yellow');
        break;
}